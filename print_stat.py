# Output `Hello <your-name>` in different printing methods

name = input("Enter your name: ")

# Using pointer args, values separated by comma
print("Hello", name)

# Using string concatination
print("Hello " + name)

# Using format string
print(f"Hello {name}")

# Using string format method
print("Hello {}".format(name))

# Using C style printing statement
print("Hello %s" % name)
